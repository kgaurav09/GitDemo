
public class StringPractice {

	public static void main(String[] args) {
	
		String str= "Payments $100 paid";
		
		System.out.println(str.charAt(9));// it will print character at 8th index
		System.out.println(str.indexOf("$"));// it will print at what index $ is present in above string
		System.out.println(str.substring(9)); // it will print the substring after provided index

	}

}
